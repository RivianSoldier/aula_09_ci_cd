def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sum_values(num1, num2):
    res = num1 + num2
    return res

def sub(num1, num2):
    res = num1 - num2
    return res

def mult(num1, num2):
    res = num1 * num2
    return res

def div(num1, num2):
    res = num1 / num2
    return res
